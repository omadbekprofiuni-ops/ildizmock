# Apostrophe class used inside compiled patterns. Matches any single
# apostrophe glyph OR the two-character escape sequence \'.
APOST_CLASS = r"(?:\\'|['’‘ʻ])"


def build_phrase_regex(uz: str) -> re.Pattern[str]:
    """Build a regex that matches `uz` with any apostrophe glyph variant."""
    parts = [re.escape(seg) for seg in uz.split("'")]
    return re.compile(APOST_CLASS.join(parts))


# Sort by length descending so longer phrases match first
PHRASES_SORTED = sorted(PHRASES, key=lambda p: len(p[0]), reverse=True)
PHRASE_REGEXES: list[tuple[re.Pattern[str], str]] = [
    (build_phrase_regex(uz), en) for uz, en in PHRASES_SORTED
]


# Word-boundary regex: word must be preceded by a non-letter and followed
# by a non-letter. Apostrophes are NOT in the boundary set.
_LETTER = r"A-Za-zА-Яа-я"


def build_word_regex(word: str) -> re.Pattern[str]:
    parts = [re.escape(seg) for seg in word.split("'")]
    body = APOST_CLASS.join(parts)
    return re.compile(rf"(?<![{_LETTER}]){body}(?![{_LETTER}])")


WORDS_SORTED = sorted(WORDS, key=lambda p: len(p[0]), reverse=True)
WORD_REGEXES: list[tuple[re.Pattern[str], str]] = [
    (build_word_regex(uz), en) for uz, en in WORDS_SORTED
]


def apply(text: str) -> tuple[int, str]:
    n = 0
    # Phase A — multi-word phrase regexes (apostrophe-tolerant)
    for rx, en in PHRASE_REGEXES:
        # Use a callable replacement to escape backreferences in `en`
        new_text, count = rx.subn(lambda _m, e=en: e, text)
        if count:
            text = new_text
            n += count
    # Phase B — word-boundary single tokens
    for rx, en in WORD_REGEXES:
        new_text, count = rx.subn(lambda _m, e=en: e, text)
        if count:
            text = new_text
            n += count
    return n, text


def main() -> None:
    total_files = 0
    total_repl = 0
    for root in (FRONTEND, BACKEND):
        if not root.exists():
            continue
        for f in root.rglob("*"):
            if not f.is_file():
                continue
            if f.suffix not in {".tsx", ".ts", ".jsx", ".js", ".py"}:
                continue
            parts = set(f.parts)
            if "migrations" in parts or "__pycache__" in parts:
                continue
            if ".venv" in parts or "venv" in parts or "node_modules" in parts:
                continue
            try:
                original = f.read_text(encoding="utf-8")
            except Exception:
                continue
            n, new = apply(original)
            if n > 0:
                f.write_text(new, encoding="utf-8")
                total_files += 1
                total_repl += n
                print(f"  {n:3} replacements in {f.relative_to(ROOT)}")
    print(f"\nFiles modified: {total_files} | Total replacements: {total_repl}")


if __name__ == "__main__":
    main()
