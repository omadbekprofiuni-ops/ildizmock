#!/usr/bin/env python3
"""Apply Uzbek -> English replacements across frontend src and backend apps.

Two-phase approach:
  Phase A: literal substring replacement of multi-word phrases (longest first).
  Phase B: regex word-boundary replacement of single Uzbek labels — these
           must NOT match when they appear inside a longer Uzbek inflection
           (e.g. "Talaba" must not match inside "Talabalar").

Each Uzbek source string is expanded to ASCII / curly / backslash-escaped
apostrophe variants automatically.

Run from repo root:
    python3 scripts/translate_uzbek.py
"""
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
FRONTEND = ROOT / "frontend" / "src"
BACKEND = ROOT / "backend" / "apps"

APOSTROPHES = ["'", "’", "‘", "ʻ"]


def variants(uz: str) -> list[str]:
    out = {uz}
    if "'" in uz:
        for glyph in APOSTROPHES:
            out.add(uz.replace("'", glyph))
        out.add(uz.replace("'", "\\'"))
    return sorted(out, key=len, reverse=True)


